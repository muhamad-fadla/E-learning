            $user_instance = User::query()
                                ->with('roles')
                                ->orderByDesc('created_at');

            return datatables()->eloquent($user_instance)
                    ->addIndexColumn()   
                    ->make(true);

        }

        $roles = Role::latest()->get();

        return view('admin.pages.users.index', compact('roles'));
    }

